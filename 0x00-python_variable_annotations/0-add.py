#!/usr/bin/env python3
"""
Task 0. Basic annotations - add

Returns sum of floats
"""


def add(a: float, b: float) -> float:
    """add
    
    This function takes 2 floats and adds them then returns it.

    Arguemnts:
        a (float): The first float to add.
        b (float): The second float to add.
    
    Return:
        (float): The sum of a & b.
    """
    return (a + b)