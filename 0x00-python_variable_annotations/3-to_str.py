"""
Task 3. Basic annotations - to string

Takes a float and returns it's string representation
"""


def to_str(n: float) -> str:
    """to_str

    Takes a float and returns a string representation
    of that float

    Arguments:
        n (float): The inputed float.

    Return:
        (str): The string representation of the inputed float.
    """
    return (str(n))
